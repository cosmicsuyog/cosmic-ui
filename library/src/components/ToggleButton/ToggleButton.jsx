import React, { useEffect, useMemo, useState } from "react";

const colors = {
  bg: "#ffffff",
  text: "#111827",
  muted: "#6b7280",
  border: "#e5e7eb",
  soft: "#f8fafc",
  accent: "#2563eb",
  success: "#16a34a",
  danger: "#dc2626",
  warning: "#d97706"
};
const buttonBase = ({
  backgroundColor = colors.accent,
  textColor = "#ffffff",
  borderRadius = "6px",
  padding = "10px 14px"
} = {}) => ({
  backgroundColor,
  color: textColor,
  border: "none",
  borderRadius,
  padding,
  fontSize: "14px",
  fontWeight: 600,
  cursor: "pointer",
  fontFamily: "inherit"
});
export const ToggleButton = ({
  pressed = false,
  children = "Toggle",
  onToggle = () => {}
}) => <button onClick={() => onToggle(!pressed)} style={buttonBase({
  backgroundColor: pressed ? colors.accent : colors.soft,
  textColor: pressed ? "#fff" : colors.text
})}>{children}</button>;

export default ToggleButton;
